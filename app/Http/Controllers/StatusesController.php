     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //need middilware
        //need validation
        $this->validate($request, ['body'=>'required']);
        //create the status
        //delegate the user and diffine statuses function in user model
        $status = User::find(1)
                    ->statuses()
                    ->create($request->only(['body']));
        //return it, and also include the user(that need for user->name) useing basic laravel elquent staf
        return $status->load('user');

    }

